INSERT INTO center_inventory (center_id, blood_type, quantity, needed_quantity)
SELECT
  c.id,
  bt.blood_type,
  floor(random() * 30 + 5)::int  AS quantity,
  floor(random() * 15 + 1)::int  AS needed_quantity
FROM centers c
CROSS JOIN (
  VALUES ('A+'),('A-'),('B+'),('B-'),('AB+'),('AB-'),('O+'),('O-')
) AS bt(blood_type)
WHERE c.city = 'Damascus'
ON CONFLICT (center_id, blood_type) DO UPDATE
  SET quantity        = EXCLUDED.quantity,
      needed_quantity = EXCLUDED.needed_quantity;
