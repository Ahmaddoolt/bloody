  SELECT
    op.id,
    an.uname,
    CASE
      WHEN (op.rn % 10) IN (0,1,2,3,4,5,6) THEN 'Damascus'
      WHEN (op.rn % 10) = 7               THEN 'Aleppo'
      WHEN (op.rn % 10) = 8               THEN 'Homs'
      ELSE                                     'Latakia'
    END AS new_city
  FROM ordered_profiles op
  JOIN arabic_names an ON an.rn = ((op.rn - 1) % 40) + 1
)
UPDATE profiles p
SET
  username = a.uname,
  city     = a.new_city
FROM assigned a
WHERE p.id = a.id;


